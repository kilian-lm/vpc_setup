#!/bin/bash

# Create the main project directory and the templates subdirectory
mkdir -p your_project/templates

# Navigate into the project directory
cd your_project

# Create the app.py file
touch app.py

# Navigate into the templates directory
cd templates

# Create the HTML template files
touch dashboard.html search_results.html breach_results.html resources.html

# Confirmation message
echo "Directory structure created successfully!"

